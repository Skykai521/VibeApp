package com.vibe.build.engine.dex

import android.content.Context
import android.util.Log
import com.android.tools.r8.CompilationMode
import com.android.tools.r8.D8
import com.android.tools.r8.D8Command
import com.android.tools.r8.Diagnostic
import com.android.tools.r8.DiagnosticsHandler
import com.android.tools.r8.OutputMode
import com.tyron.builder.BuildModule
import java.io.File
import java.security.MessageDigest
import java.util.zip.ZipEntry
import java.util.zip.ZipInputStream
import java.util.zip.ZipOutputStream

/**
 * Caches pre-dexed versions of large library JARs (androidx-classes.jar,
 * shadow-runtime.jar) so they don't need to be re-dexed on every build.
 *
 * The cache is invalidated when the source JAR content changes (MD5 hash).
 * First build pays the D8 cost (~15-25s); subsequent builds skip it entirely.
 */
object PreDexCache {

    private const val TAG = "PreDexCache"
    private const val CACHE_DIR_NAME = "predex-cache"
    // Bump when the pre-dex pipeline changes (e.g. R-class stripping added
    // in v2) to invalidate stale caches on device.
    private const val CACHE_VERSION = 2

    data class CachedDexFiles(
        val dexFiles: List<File>,
    )

    /**
     * Returns pre-dexed DEX files for all library JARs.
     * Runs D8 on first call or when JARs change; returns cached files otherwise.
     */
    fun getOrCreateLibraryDex(context: Context, minSdk: Int): CachedDexFiles {
        val cacheDir = File(context.filesDir, CACHE_DIR_NAME)
        cacheDir.mkdirs()

        val jarsToPreDex = buildList {
            BuildModule.getAndroidxClassesJar()?.takeIf { it.exists() }?.let { add(it) }
            BuildModule.getShadowRuntimeJar()?.takeIf { it.exists() }?.let { add(it) }
        }

        if (jarsToPreDex.isEmpty()) {
            return CachedDexFiles(emptyList())
        }

        val currentHash = computeCombinedHash(jarsToPreDex)
        val hashFile = File(cacheDir, "predex.md5")
        val dexOutputDir = File(cacheDir, "dex")

        // Check cache validity
        if (dexOutputDir.exists() && hashFile.exists() && hashFile.readText() == currentHash) {
            val cachedDexFiles = collectDexFiles(dexOutputDir)
            if (cachedDexFiles.isNotEmpty()) {
                Log.d(TAG, "Cache hit: ${cachedDexFiles.size} pre-dexed files")
                return CachedDexFiles(cachedDexFiles)
            }
        }

        // Cache miss — run D8
        Log.d(TAG, "Cache miss, pre-dexing ${jarsToPreDex.size} JARs (${jarsToPreDex.sumOf { it.length() } / 1024}KB)...")
        val startTime = System.currentTimeMillis()

        // Use a temp dir for atomic replacement
        val tempDir = File(cacheDir, "dex-tmp")
        tempDir.deleteRecursively()
        tempDir.mkdirs()

        try {
            runD8(jarsToPreDex, tempDir, minSdk)

            // Atomic swap
            dexOutputDir.deleteRecursively()
            tempDir.renameTo(dexOutputDir)
            hashFile.writeText(currentHash)

            val elapsed = System.currentTimeMillis() - startTime
            val dexFiles = collectDexFiles(dexOutputDir)
            Log.d(TAG, "Pre-dex complete in ${elapsed}ms, produced ${dexFiles.size} DEX files")
            return CachedDexFiles(dexFiles)
        } catch (e: Exception) {
            Log.e(TAG, "Pre-dex failed, falling back to full DEX", e)
            tempDir.deleteRecursively()
            // Return empty so D8DexConverter falls back to including JARs directly
            return CachedDexFiles(emptyList())
        }
    }

    /**
     * Checks if pre-dex cache is valid (used by D8DexConverter to decide
     * whether to skip library JARs).
     */
    fun isCacheValid(context: Context): Boolean {
        val cacheDir = File(context.filesDir, CACHE_DIR_NAME)
        val hashFile = File(cacheDir, "predex.md5")
        val dexOutputDir = File(cacheDir, "dex")
        if (!hashFile.exists() || !dexOutputDir.exists()) return false

        val jars = buildList {
            BuildModule.getAndroidxClassesJar()?.takeIf { it.exists() }?.let { add(it) }
            BuildModule.getShadowRuntimeJar()?.takeIf { it.exists() }?.let { add(it) }
        }
        if (jars.isEmpty()) return false

        return hashFile.readText() == computeCombinedHash(jars) &&
            collectDexFiles(dexOutputDir).isNotEmpty()
    }

    private fun runD8(jars: List<File>, outputDir: File, minSdk: Int) {
        // Strip R classes from library JARs before DEXing. The authoritative
        // R classes are generated by AAPT2 per-build and compiled into the
        // user DEX (classes.dex). Pre-dex R classes have stale resource IDs
        // from the original library build and MUST NOT be present at runtime,
        // otherwise they shadow the correct AAPT2 versions and cause resource
        // ID mismatches (e.g. Snackbar inflating the wrong layout).
        val filteredJars = jars.map { jar -> stripRClasses(jar, outputDir) }

        val handler = object : DiagnosticsHandler {
            override fun error(d: Diagnostic) { Log.e(TAG, d.diagnosticMessage) }
            override fun warning(d: Diagnostic) { Log.w(TAG, d.diagnosticMessage) }
            override fun info(d: Diagnostic) { Log.d(TAG, d.diagnosticMessage) }
        }

        val command = D8Command.builder(handler)
            .addProgramFiles(filteredJars.map { it.toPath() })
            .addLibraryFiles(
                listOfNotNull(
                    BuildModule.getAndroidJar()?.toPath(),
                    BuildModule.getLambdaStubs()?.toPath(),
                ),
            )
            .setMinApiLevel(minSdk)
            .setMode(CompilationMode.RELEASE)
            .setOutput(outputDir.toPath(), OutputMode.DexIndexed)
            .build()
        D8.run(command)

        // Clean up filtered JARs
        filteredJars.forEach { it.delete() }
    }

    /**
     * Creates a copy of the JAR with all R.class and R$*.class files removed.
     */
    private fun stripRClasses(jar: File, workDir: File): File {
        val filtered = File(workDir, "filtered-${jar.name}")
        ZipInputStream(jar.inputStream().buffered()).use { zis ->
            ZipOutputStream(filtered.outputStream().buffered()).use { zos ->
                generateSequence { zis.nextEntry }.forEach { entry ->
                    val name = entry.name
                    if (!isRClass(name)) {
                        zos.putNextEntry(ZipEntry(name))
                        zis.copyTo(zos)
                        zos.closeEntry()
                    }
                }
            }
        }
        Log.d(TAG, "Stripped R classes from ${jar.name} → ${filtered.name}")
        return filtered
    }

    private fun isRClass(entryName: String): Boolean {
        val simple = entryName.substringAfterLast('/')
        return simple == "R.class" || simple.startsWith("R\$")
    }

    private fun collectDexFiles(dir: File): List<File> {
        if (!dir.exists()) return emptyList()
        return dir.listFiles { f -> f.isFile && f.name.endsWith(".dex") }
            ?.sortedBy { it.name }
            .orEmpty()
    }

    private fun computeCombinedHash(files: List<File>): String {
        val digest = MessageDigest.getInstance("MD5")
        // Include cache version so pipeline changes invalidate old caches
        digest.update("v$CACHE_VERSION".toByteArray())
        files.sortedBy { it.name }.forEach { file ->
            file.inputStream().buffered().use { input ->
                val buffer = ByteArray(8192)
                var read: Int
                while (input.read(buffer).also { read = it } != -1) {
                    digest.update(buffer, 0, read)
                }
            }
        }
        return digest.digest().joinToString("") { "%02x".format(it) }
    }
}
